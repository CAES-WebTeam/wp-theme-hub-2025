/**
 * Modifies the Yoast SEO schema to include multiple authors from the 'authors' ACF repeater field.
 * This function handles both WordPress Users and Custom manual entries.
 *
 * @param array $data The Schema Article data generated by Yoast.
 * @return array The modified Schema Article data.
 */
function caes_add_acf_authors_to_yoast_schema( $data ) {
    
    // Get the 'authors' repeater field for the current post.
    $authors = get_field('authors');

    // Proceed only if the repeater field has data.
    if ( $authors ) {
        $authors_schema = []; // Initialize an empty array for our author schema objects.

        // Loop through each row of the repeater.
        foreach ( $authors as $author_row ) {
            
            $entry_type = $author_row['type'] ?? '';
            $person_schema = null;

            // Check if it's a 'Custom' (manual) entry.
            if ( $entry_type === 'Custom' ) {
                $custom_user = $author_row['custom_user'] ?? $author_row['custom'] ?? [];
                $first_name = $custom_user['first_name'] ?? '';
                $last_name = $custom_user['last_name'] ?? '';
                $full_name = trim("$first_name $last_name");

                if ( ! empty($full_name) ) {
                    // Build a Person schema object WITHOUT a URL.
                    $person_schema = [
                        '@type' => 'Person',
                        'name'  => $full_name,
                    ];
                }
            } 
            // Otherwise, handle it as a WordPress User entry.
            else {
                $user_data = $author_row['user'] ?? null;
                $user_id = is_array($user_data) ? ($user_data['ID'] ?? null) : $user_data;
                
                if ( $user_id && is_numeric($user_id) ) {
                    $display_name = get_the_author_meta('display_name', $user_id);
                    $profile_url = get_author_posts_url($user_id);

                    // Build a Person schema object WITH a URL.
                    $person_schema = [
                        '@type' => 'Person',
                        'name'  => $display_name,
                        'url'   => $profile_url,
                    ];
                }
            }

            // If a valid person schema was created, add it to our array.
            if ($person_schema) {
                $authors_schema[] = $person_schema;
            }
        }

        // If we have authors, replace the default 'author' data with our new array.
        if ( ! empty($authors_schema) ) {
            $data['author'] = $authors_schema;
        }
    }

    return $data;
}

add_filter( 'wpseo_schema_article', 'caes_add_acf_authors_to_yoast_schema', 11, 1 );


/**
 * Remove the default Author/Person piece from the schema graph when we have custom ACF authors.
 * This is the proper Yoast way to prevent schema pieces from being output.
 *
 * @param array $pieces The array of schema piece generators.
 * @param \Yoast\WP\SEO\Context\Meta_Tags_Context $context The context object.
 * @return array The filtered array of schema pieces.
 */
function caes_remove_default_author_from_graph( $pieces, $context ) {
    
    // Only filter on singular posts/pages
    if ( ! is_singular() ) {
        return $pieces;
    }
    
    // Check if we have custom ACF authors
    $custom_authors = get_field('authors');
    if ( empty($custom_authors) ) {
        return $pieces;
    }
    
    // Remove the Author piece (which generates the Person schema for the post author)
    return array_filter( $pieces, function( $piece ) {
        // Remove if it's an Author piece
        return ! $piece instanceof \Yoast\WP\SEO\Generators\Schema\Author;
    });
}

add_filter( 'wpseo_schema_graph_pieces', 'caes_remove_default_author_from_graph', 11, 2 );


/**
 * Replace the default author meta tag with multiple authors from ACF.
 * This changes the <meta name="author" content="..."> tag in the HTML head.
 *
 * @param string $author The author name from Yoast.
 * @param object $presentation The presentation object (available in Yoast 19.3+).
 * @return string The modified author name(s).
 */
function caes_change_meta_author_tag( $author, $presentation = null ) {
    
    // Only modify on singular posts
    if ( ! is_singular() ) {
        return $author;
    }
    
    // Get the ACF authors
    $authors = get_field('authors');
    
    // If no ACF authors, return the default
    if ( empty($authors) ) {
        return $author;
    }
    
    // Build an array of author names
    $author_names = [];
    
    foreach ( $authors as $author_row ) {
        $entry_type = $author_row['type'] ?? '';
        
        // Handle Custom entries
        if ( $entry_type === 'Custom' ) {
            $custom_user = $author_row['custom_user'] ?? $author_row['custom'] ?? [];
            $first_name = $custom_user['first_name'] ?? '';
            $last_name = $custom_user['last_name'] ?? '';
            $full_name = trim("$first_name $last_name");
            
            if ( ! empty($full_name) ) {
                $author_names[] = $full_name;
            }
        } 
        // Handle WordPress User entries
        else {
            $user_data = $author_row['user'] ?? null;
            $user_id = is_array($user_data) ? ($user_data['ID'] ?? null) : $user_data;
            
            if ( $user_id && is_numeric($user_id) ) {
                $display_name = get_the_author_meta('display_name', $user_id);
                if ( ! empty($display_name) ) {
                    $author_names[] = $display_name;
                }
            }
        }
    }
    
    // Join multiple authors with commas
    if ( ! empty($author_names) ) {
        return implode(', ', $author_names);
    }
    
    return $author;
}

add_filter( 'wpseo_meta_author', 'caes_change_meta_author_tag', 10, 2 );


/**
 * Remove Yoast's default author meta tag so we can add our own.
 * This ensures we have full control over the author meta tag output.
 *
 * @param array $presenters The array of presenters that output meta tags.
 * @return array The filtered array without the author presenter.
 */
function caes_remove_yoast_author_meta_tag( $presenters ) {
    return array_filter( $presenters, function( $presenter ) {
        return ! $presenter instanceof \Yoast\WP\SEO\Presenters\Meta_Author_Presenter;
    });
}

add_filter( 'wpseo_frontend_presenters', 'caes_remove_yoast_author_meta_tag' );


/**
 * Add our custom author meta tag for all posts with ACF authors.
 * This outputs the meta tag directly, bypassing Yoast's conditional logic.
 */
function caes_add_author_meta_tag_manually() {
    // Only on singular posts/pages
    if ( ! is_singular() ) {
        return;
    }
    
    // Get ACF authors
    $authors = get_field('authors');
    if ( empty($authors) ) {
        return;
    }
    
    // Build author names
    $author_names = [];
    
    foreach ( $authors as $author_row ) {
        $entry_type = $author_row['type'] ?? '';
        
        if ( $entry_type === 'Custom' ) {
            $custom_user = $author_row['custom_user'] ?? $author_row['custom'] ?? [];
            $first_name = $custom_user['first_name'] ?? '';
            $last_name = $custom_user['last_name'] ?? '';
            $full_name = trim("$first_name $last_name");
            
            if ( ! empty($full_name) ) {
                $author_names[] = $full_name;
            }
        } else {
            $user_data = $author_row['user'] ?? null;
            $user_id = is_array($user_data) ? ($user_data['ID'] ?? null) : $user_data;
            
            if ( $user_id && is_numeric($user_id) ) {
                $display_name = get_the_author_meta('display_name', $user_id);
                if ( ! empty($display_name) ) {
                    $author_names[] = $display_name;
                }
            }
        }
    }
    
    // Output the meta tag if we have authors
    if ( ! empty($author_names) ) {
        $authors_string = esc_attr( implode(', ', $author_names) );
        echo '<meta name="author" content="' . $authors_string . '" class="yoast-seo-meta-tag" />' . "\n";
    }
}

add_action( 'wp_head', 'caes_add_author_meta_tag_manually', 1 );

/**
 * Modify publication title tag to include subtitle and page number
 * Works with Yoast SEO
 */
function caes_modify_publication_title_tag($title) {
    // Only run on publications
    if (!is_singular('publications')) {
        return $title;
    }
    
    // Get the current page number and ensure it's a positive integer
    $page = absint(get_query_var('page'));
    
    // Check if there's a subtitle ACF field
    $subtitle = get_field('subtitle');
    
    // If we're on page 1 and there's no subtitle, return original title
    if ($page < 2 && empty($subtitle)) {
        return $title;
    }
    
    // Find the position of the separator (Yoast separator)
    $separators = array(' | ', ' - ', ' • ', ' · ', ' « ', ' » ', ' < ', ' > ');
    $separator_found = false;
    $separator_pos = false;
    
    foreach ($separators as $separator) {
        $pos = strpos($title, $separator);
        if ($pos !== false) {
            $separator_pos = $pos;
            $separator_found = true;
            break;
        }
    }
    
    // Build what we're inserting
    $insert_text = '';
    if (!empty($subtitle)) {
        $insert_text .= ': ' . $subtitle;
    }
    if ($page >= 2) {
        $insert_text .= ' - Page ' . absint($page);
    }
    
    // Insert before the separator if found, otherwise append
    if ($separator_found) {
        $before_separator = substr($title, 0, $separator_pos);
        $after_separator = substr($title, $separator_pos);
        
        return $before_separator . $insert_text . $after_separator;
    }
    
    // No separator found, just append
    return $title . $insert_text;
}
add_filter('wpseo_title', 'caes_modify_publication_title_tag', 10, 1);

// Fallback for if Yoast is disabled
add_filter('pre_get_document_title', 'caes_modify_publication_title_tag', 10, 1);