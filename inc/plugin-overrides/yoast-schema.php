 */
function caes_add_article_schema_to_custom_post_types( $post_types ) {
    $post_types[] = 'shorthand_story';
    return $post_types;
}

add_filter( 'wpseo_schema_article_post_types', 'caes_add_article_schema_to_custom_post_types' );


